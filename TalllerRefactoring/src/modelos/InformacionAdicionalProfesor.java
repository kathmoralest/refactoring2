package modelos;

public class InformacionAdicionalProfesor {
    public int aniosdeTrabajo;
    public String facultad;
    public double BonoFijo;
    
    public int getAniosTrabajo() {
    	return this.aniosdeTrabajo;
    }
    
    public void setAniosTrabajo(int aniosdeTrabajo) {
    	this.aniosdeTrabajo = aniosdeTrabajo;
    }
    
    public String getFacultad() {
    	return this.facultad;
    }
    
    public void setFacultad(String facultad) {
    	this.facultad = facultad;
    }
    
    public double setBonoFijo() {
    	return this.BonoFijo;
    }
    
    public void setBonoFijo(double bonoFijo) {
    	this.BonoFijo = bonoFijo;
    }
}



